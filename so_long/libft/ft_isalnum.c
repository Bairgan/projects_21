#include "libft.h"

int	ft_isalnum(int ch)
{
	int	i;

	if (((ch >= 48) && (ch <= 57)) || ((ch >= 65) && (ch <= 90))
		|| ((ch >= 97) && (ch <= 122)))
		i = 1;
	else
		i = 0;
	return (i);
}
