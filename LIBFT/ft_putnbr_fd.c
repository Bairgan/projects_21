#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int		i;
	char	str[11];

	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n = n * -1;
	}
	i = 0;
	if (n == 0)
		write(fd, "0", 1);
	while (n != 0)
	{
		str[i] = n % 10 + '0';
		n = n / 10;
		i++;
	}
	while (i--)
		write(fd, &str[i], 1);
}
